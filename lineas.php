      <h3>Red de Proyectistas</h3>
      <p>La UBA considera parte importante del quehacer universitario todo lo relativo con la investigación para generar nuevos conocimientos y propiciar procesos de innovación que contribuyan con el desarrollo nacional.</p>
      <p>La Red de Proyectistas para el fortalecimiento de las capacidades de Desarrollo e Innovación de la universidad (REDPDIUBA), focalizado en las distintas áreas de experticia de acuerdo a los diferentes programas formativos es un ente adscrito al Vicerrectorado Académico pero con disfrute de autonomía de actuación y capacidad propia de auto sostenimiento, ideado como eje alrededor del cual se articulan los cuatro centros de las Facultades, para brindar soporte especializado al pre y postgrado de la UBA; así como a instituciones del sector.</p>
      <p>De igual modo, se concibe como soporte a las comunidades en materia de ideas de proyectos cimentados en la metodología de Aprendizaje Basado en Emprendimiento por Proyecto (ABEP). Se asume como un generador de vínculos para responder proactivamente a problemáticas institucionales y sociales; para lo que deberá apoyarse en organizaciones aliadas, en centros especializados en diferentes áreas, otras universidades y centros de desarrollo, investigación e innovación, instituciones educativas y redes nacionales e internacionales. Su eje de conocimiento es la persona: el proyectista en sus diversos roles: el que recopila información, elabora proyectos, los ejecuta y se responsabiliza con las personas de la comunidad beneficiaria.</p>
      <p>La propuesta de creación de la Red como eje integrador de los cuatros centros de proyectos en las áreas relacionadas con el pre y postgrado de la UBA, tal como se muestra en la figura 1, se fundamenta en la importancia que tiene el desarrollo y la innovación en el funcionamiento global de la Universidad y en la necesidad de formalizar la organización de un espacio específico destinado para tal fin en el ámbito académico y social enmarcado en las prioridades de la nación; constituyéndose en un elemento clave en el desarrollo y gestión institucional en el marco de los proyectos ABEP.</p>
      
