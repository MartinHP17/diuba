      <h3>Líneas de Investigación Asociada</h3>
      <p>Cada una de las líneas de investigación institucional se articulan adecuadamente a un conjunto de líneas asociadas de las cuales se desprenden las áreas temáticas que se vinculan debidamente a las líneas de investigación institucional y contribuyen a generar proyectos con pertinencia social en el contexto local, regional, nacional e internacional.</p>
      <p>Para la determinación de las líneas asociadas de investigación de cada Escuela que dan lugar a rutas específicas de investigación, permite no solo diversidad de enfoques, sino también fortalecimiento de las especificidades de cada uno de las unidades curriculares, articulando las áreas temáticas y los tópicos emergentes previamente definidos a una o más líneas de investigación institucional de acuerdo a sus intereses y a la pertinencia de estas con los proyectos.</p>
      <p>Las Líneas Institucionales y las Líneas Asociadas no son espacios exclusivos de una única facultad ni de un único programa, existe la posibilidad de que las líneas asociadas se ubiquen en áreas de convergencia entre dos o más líneas de investigación institucional dependiendo del alcance de la investigación y el propósito del estudio (la meta a largo plazo), estará representando la líneas de Investigación institucional, es decir, el conocimiento que se pretende aportar.</p>
      
